    private void txtNIPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNIPActionPerformed
        txtNama.setEnabled(false);
        txtBonus.setEnabled(true);
        txtBonus.requestFocus();
        btnBersih.setEnabled(true);
        btnDelete.setEnabled(true);
        txtGP.setEnabled(false);
        String nama = null;
        String jabatan = null;
        int gp = 0;
        if(null!=txtNIP.getText())switch (txtNIP.getText()) {
            case "NK001":
                nama = "Basuki";
                jabatan = "DIREKTUR UTAMA";
                gp=15000000;
                break;
            case "NK002":
                nama = "Bambang Purnama";
                jabatan = "SUPERVISOR";
                gp=10000000;
                break;
            case "NK003":
                nama = "Budi Bagus";
                jabatan = "ASSISTEN MANAGER";
                gp=5500000;
                break;
            case "NK004":
                nama = "Mutiara Mita Cahya";
                jabatan = "MANAGER";
                gp=8500000;
                break;
            default:
                break;
        }
        txtNama.setText(String.valueOf(nama));
        cmbJabatan.setSelectedItem(jabatan);
        txtGP.setText(String.valueOf(gp));
    }//GEN-LAST:event_txtNIPActionPerformed

