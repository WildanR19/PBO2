    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        btnDelete.setEnabled(true);
        tblGaji.setEnabled(true);
        txtBonus.setEnabled(false);
        txtGP.setEnabled(true);
        txtNama.setEnabled(false);
        cmbJabatan.setEnabled(false);
        double pot;
        int gb, gp, gt, bonus;
        gp = Integer.parseInt(txtGP.getText());
        pot = gp*0.05;
        gb = gp-(int)pot;
        bonus = Integer.parseInt(txtBonus.getText());
        gt =  gb + bonus;
        
        DefaultTableModel dataModel = (DefaultTableModel) tblGaji.getModel();
        List list = new ArrayList<>();
        tblGaji.setAutoCreateColumnsFromModel(true);
        list.add(txtNIP.getText());
        list.add(txtNama.getText());
        list.add(cmbJabatan.getSelectedItem());
        list.add("Rp. "+gb);
        list.add("Rp. "+pot);
        list.add("Rp. "+gt);
        dataModel.addRow(list.toArray());
        
       
        txtBonus.setText("");
        txtGP.setText("");
        txtNIP.setText("");
        txtNIP.requestFocus();
        txtNama.setText("");
        cmbJabatan.setSelectedItem("-- Pilih --");
       
    }//GEN-LAST:event_btnSaveActionPerformed

