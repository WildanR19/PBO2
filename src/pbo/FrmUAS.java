    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int brs;
        DefaultTableModel dataModel = (DefaultTableModel) tblGaji.getModel();
        brs=tblGaji.getSelectedRow();
        if(brs>=0){
            int opsi=JOptionPane.showConfirmDialog(null, "Yakin Dihapus ?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (opsi==JOptionPane.YES_OPTION){
                dataModel.removeRow(brs);
            }
        }
        txtNIP.requestFocus();
    }//GEN-LAST:event_btnDeleteActionPerformed

